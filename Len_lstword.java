class Solution {
    public int lengthOfLastWord(String s) {
        int len=0;
        String a=s.trim();
        for(int i=0; i<a.length(); i++)
        {
            if(a.charAt(i)== ' ')
            {
                len=0;
            }
            else
            {
                len++;
            }
        }
        return len;
    }
}
